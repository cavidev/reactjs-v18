import { useQuery } from "@tanstack/react-query";
import { useCurrentWeatherApiProps } from "types/weatherTypes";
import { headers } from "~/utils/noUpload";
import { baseUrl, fetchTypedData, mockFetchTypedData } from "./utils";

const useForecastWeatherApi = ({ lat, lon, isDev = false }: useCurrentWeatherApiProps) => {
    const RapidApi = new URL("/forecast.json", baseUrl);
    RapidApi.searchParams.set("q", `${lat},${lon}`);
    RapidApi.searchParams.set("lang", "es");
    RapidApi.searchParams.set("days", "3");
    RapidApi.searchParams.set("alerts", "yes");
    const url = RapidApi.toString();
    const options = {
        method: "GET",
        headers,
    };

    return useQuery({
        queryKey: ["useForecastWeatherApi"],
        queryFn: async () => {
            if (!isDev) {
                const response = await fetchTypedData<typeof forecastApi, typeof options>(url, options);
                return response;
            }
            return await mockFetchTypedData(1000, forecastApi);
        },
    });
};

